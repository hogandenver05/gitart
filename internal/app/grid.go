func BuildGrid(message string) Grid {
	rows := 5
	grid := make(Grid, rows)

	for _, character := range message {
		pattern, ok := Font[character]

		if !ok {
			pattern = Font[' ']
		}

		for r := range rows {
			grid[r] = append(grid[r], pattern[r]...)
			grid[r] = append(grid[r], 0)
		}
	}

	return grid
}
